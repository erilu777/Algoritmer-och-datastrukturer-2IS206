package Lab4;

/**
 * A key-value pair.
 */
public class Entry {
    public int key, value;

    /**
     * Create a key-value pair.
     *
     * @param key the key
     * @param value the value
     */
    public Entry(int key, int value) {
        this.key = key;
        this.value = value;
    }
}